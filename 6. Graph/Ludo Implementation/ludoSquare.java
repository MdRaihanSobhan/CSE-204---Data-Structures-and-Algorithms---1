public class ludoSquare {
    int index, height, parent;

    public ludoSquare() {
        this.index = -1;
        this.height = -1;
        this.parent = -1;
    }

    public ludoSquare(int index, int height, int parent) {
        this.index = index;
        this.height = height;
        this.parent = parent;
    }
}
